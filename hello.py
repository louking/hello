from app import app
import flask
from flask.views import MethodView

#######################################################################
class Hello(MethodView):
#######################################################################
    #----------------------------------------------------------------------
    def get(self):
    #----------------------------------------------------------------------
        try:
            return flask.render_template('hello.html')
        
        except:
            raise
#----------------------------------------------------------------------
app.add_url_rule('/',view_func=Hello.as_view('index'),methods=['GET'])
#----------------------------------------------------------------------
